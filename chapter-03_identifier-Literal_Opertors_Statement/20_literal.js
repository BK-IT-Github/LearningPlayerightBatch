// 1. String Literal – Text inside quotes
let greeting = "Hello World";

// 2. Number Literal – Numeric value
let age = 25;

// 3. Boolean Literal – True or False value
let isLoggedIn = true;

// 4. Array Literal – List of values inside []
let numbers = [1, 2, 3, 4];

// 5. Object Literal – Key-value pairs inside {}
let person = { name: "John", age: 30 };

// 6. Null Literal – Intentional empty value
let data = null;

// 7. Undefined Literal – Variable declared but no value assigned
let result = undefined;

// 8. Template Literal – String using backticks and variables
let name = "Balaji";
let message = `Hello ${name}`;

// 9. RegExp Literal – Pattern used for matching text
let pattern = /abc/;

// 10. BigInt Literal – Very large integer value
let bigNumber = 12345678901234567890n;
