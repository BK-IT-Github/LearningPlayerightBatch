// Identires, Literal, Operator in JS
// Identires are the names given to the variables, functions, classes, etc.
// Identires can contain letters, numbers, underscores, and dollar signs.
// Identires cannot start with a number.
// Identires cannot be a keyword.

// Valid Identires
let myVar = 10;
let _myVar = 20;
let $myVar = 30;
let myVar1 = 40;

// Invalid Identires
// let 1myVar = 50;
// let my-Var = 60;
// let my Var = 70;
// let my_var = 80;